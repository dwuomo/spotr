<?php

namespace embryon\application\dtos\RegisterUser;


class RegisterUserResponse
{
    /** @var  boolean */
    private $status;
    /** @var  string */
    private $message;

    /**
     * @return bool
     */
    public function isStatus()
    {
        return $this->status;
    }

    /**
     * @param bool $status
     */
    public function setStatus($status)
    {
        $this->status = $status;
    }

    /**
     * @return string
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param string $message
     */
    public function setMessage($message)
    {
        $this->message = $message;
    }

}